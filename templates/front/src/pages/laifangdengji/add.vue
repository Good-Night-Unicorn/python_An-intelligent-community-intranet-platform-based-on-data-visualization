<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            laifangshijian : false,
            laifangrenyuan : false,
            shoujihaoma : false,
            zhuhuzhanghao : false,
            zhuhuxingming : false,
            loudonghao : false,
            tiwen : false,
            jinrushijian : false,
            chuqushijian : false,
            beizhu : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          laifangshijian: '',
          laifangrenyuan: '',
          shoujihaoma: '',
          zhuhuzhanghao: '',
          zhuhuxingming: '',
          loudonghao: '',
          tiwen: '',
          jinrushijian: '',
          chuqushijian: '',
          beizhu: '',
        },
        rules: {
          laifangshijian: [
          ],
          laifangrenyuan: [
          ],
          shoujihaoma: [
          ],
          zhuhuzhanghao: [
          ],
          zhuhuxingming: [
          ],
          loudonghao: [
          ],
          tiwen: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          jinrushijian: [
          ],
          chuqushijian: [
          ],
          beizhu: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.laifangshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='laifangshijian'){
              this.ruleForm.laifangshijian = obj[o];
              this.ro.laifangshijian = true;
              continue;
            }
            if(o=='laifangrenyuan'){
              this.ruleForm.laifangrenyuan = obj[o];
              this.ro.laifangrenyuan = true;
              continue;
            }
            if(o=='shoujihaoma'){
              this.ruleForm.shoujihaoma = obj[o];
              this.ro.shoujihaoma = true;
              continue;
            }
            if(o=='zhuhuzhanghao'){
              this.ruleForm.zhuhuzhanghao = obj[o];
              this.ro.zhuhuzhanghao = true;
              continue;
            }
            if(o=='zhuhuxingming'){
              this.ruleForm.zhuhuxingming = obj[o];
              this.ro.zhuhuxingming = true;
              continue;
            }
            if(o=='loudonghao'){
              this.ruleForm.loudonghao = obj[o];
              this.ro.loudonghao = true;
              continue;
            }
            if(o=='tiwen'){
              this.ruleForm.tiwen = obj[o];
              this.ro.tiwen = true;
              continue;
            }
            if(o=='jinrushijian'){
              this.ruleForm.jinrushijian = obj[o];
              this.ro.jinrushijian = true;
              continue;
            }
            if(o=='chuqushijian'){
              this.ruleForm.chuqushijian = obj[o];
              this.ro.chuqushijian = true;
              continue;
            }
            if(o=='beizhu'){
              this.ruleForm.beizhu = obj[o];
              this.ro.beizhu = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.zhuhuzhanghao!=''&&json.zhuhuzhanghao) || json.zhuhuzhanghao==0){
                this.ruleForm.zhuhuzhanghao = json.zhuhuzhanghao
            }
            if((json.zhuhuxingming!=''&&json.zhuhuxingming) || json.zhuhuxingming==0){
                this.ruleForm.zhuhuxingming = json.zhuhuxingming
            }
            if((json.loudonghao!=''&&json.loudonghao) || json.loudonghao==0){
                this.ruleForm.loudonghao = json.loudonghao
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('laifangdengji/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        if(this.ruleForm.jinrushijian>this.ruleForm.chuqushijian){
          this.$message.error("进入时间不能超过出去时间");
          return
        }
        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('laifangdengji/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('laifangdengji/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('laifangdengji/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

